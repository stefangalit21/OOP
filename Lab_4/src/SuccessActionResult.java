/**
 * Represents a successful action result.
 * This class extends the ActionResult class to specifically handle successful outcomes.
 */
public class SuccessActionResult extends ActionResult {
    
    /**
     * Constructs a new SuccessActionResult with the specified message.
     *
     * @param message A String containing the success message for this action result.
     */
    public SuccessActionResult(String message) {
        super(message);
    }
}


